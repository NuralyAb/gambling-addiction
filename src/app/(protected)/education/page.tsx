function LessonQuiz({
  quiz,
  lessonId,
  onComplete,
}: {
  quiz: Quiz[];
  lessonId: string;
  onComplete: () => void;
}) {
  const [answers, setAnswers] = useState<(number | null)[]>(Array(quiz.length).fill(null));
  const [submitted, setSubmitted] = useState(false);

  const allAnswered = answers.every((a) => a !== null);
  const allCorrect = quiz.every((q, i) => answers[i] === q.correct);

  const handleSubmit = () => {
    setSubmitted(true);
    if (allCorrect) {
      onComplete();
    }
  };

  const handleRetry = () => {
    setAnswers(Array(quiz.length).fill(null));
    setSubmitted(false);
  };

  return (
    <div className="mt-6 border-t border-dark-border pt-6">
      <p className="text-sm font-medium text-white mb-4">üìù –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å–µ–±—è</p>
      <div className="space-y-5">
        {quiz.map((q, qi) => (
          <div key={qi}>
            <p className="text-sm text-slate-300 mb-2">
              {qi + 1}. {q.question}
            </p>
            <div className="space-y-1.5">
              {q.options.map((opt, oi) => {
                let optClass = "bg-dark-lighter/50 border border-dark-border hover:border-accent/30";
                if (submitted) {
                  if (oi === q.correct) {
                    optClass = "bg-green-500/10 border border-green-500/30";
                  } else if (answers[qi] === oi && oi !== q.correct) {
                    optClass = "bg-red-500/10 border border-red-500/30";
                  }
                } else if (answers[qi] === oi) {
                  optClass = "bg-accent/10 border border-accent/30";
                }

                return (
                  <button
                    key={oi}
                    onClick={() => {
                      if (submitted) return;
                      const next = [...answers];
                      next[qi] = oi;
                      setAnswers(next);
                    }}
                    className={`w-full text-left px-3 py-2 rounded-lg text-sm transition-colors ${optClass} ${
                      submitted ? "cursor-default" : "cursor-pointer"
                    }`}
                  >
                    <span className="text-slate-300">{opt}</span>
                  </button>
                );
              })}
            </div>
          </div>
        ))}
      </div>
      <div className="mt-4 flex items-center gap-3">
        {!submitted ? (
          <Button size="sm" disabled={!allAnswered} onClick={handleSubmit}>
            –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –æ—Ç–≤–µ—Ç—ã
          </Button>
        ) : allCorrect ? (
          <div className="flex items-center gap-2 text-green-400 text-sm">
            <span>‚úÖ</span>
            <span>–û—Ç–ª–∏—á–Ω–æ! –í—Å–µ –æ—Ç–≤–µ—Ç—ã –≤–µ—Ä–Ω—ã. –£—Ä–æ–∫ –ø—Ä–æ–π–¥–µ–Ω!</span>
          </div>
        ) : (
          <>
            <p className="text-sm text-red-400">–ï—Å—Ç—å –æ—à–∏–±–∫–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.</p>
            <Button size="sm" variant="secondary" onClick={handleRetry}>
              –ü–æ–≤—Ç–æ—Ä–∏—Ç—å
            </Button>
          </>
        )}
      </div>
    </div>
  );
}

